@router.message(F.text)
async def check_translator_trigger(message: Message):
    if not message.text or message.text.startswith("/"):
        return

    chat_id = message.chat.id
    s = await repo.get_settings(chat_id)
    if not s.get("translator_enabled"):
        return

    text_lower = message.text.lower().strip()
    triggered_word = None

    for trigger in TRIGGERS:
        if re.search(trigger, text_lower):
            triggered_word = re.search(trigger, text_lower).group()
            break

    if not triggered_word:
        return

    # Probability check
    if random.random() > cfg.TRANSLATOR_TRIGGER_CHANCE:
        return

    await repo.log_translator(chat_id, message.from_user.id, triggered_word)
    response = random.choice(RESPONSES)
    await message.reply(response)


@router.message(Command("\u044f\u0441\u043d\u043e_\u0442\u043e\u043f"))
async def cmd_yasno_top(message: Message):
    top = await repo.get_translator_top(message.chat.id)
    if not top:
        await message.answer("\U0001f50e \u041d\u0438\u043a\u0442\u043e \u0435\u0449\u0451 \u043d\u0435 \u043f\u043e\u043f\u0430\u043b\u0441\u044f \u043f\u0435\u0440\u0435\u0432\u043e\u0434\u0447\u0438\u043a\u0443!")
        return

    lines = ["\U0001f50e <b>\u0422\u043e\u043f \u00ab\u044f\u0441\u043d\u043e\u00bb</b>\n"]
    for i, t in enumerate(top, 1):
        name = t.get("first_name") or t.get("username") or "?"
        lines.append(f"{i}. {name} \u2014 {t['cnt']} \u0440\u0430\u0437")

    await message.answer("\n".join(lines), parse_mode="HTML")
